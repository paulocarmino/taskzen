import React from 'react';
import { cn } from '@/lib/utils';

interface ContainerProps {
  children: React.ReactNode;
  className?: string;
  size?: 'sm' | 'md' | 'lg' | 'xl' | 'full';
}

const sizes = {
  sm: 'max-w-screen-sm',
  md: 'max-w-screen-md',
  lg: 'max-w-screen-lg',
  xl: 'max-w-screen-xl',
  full: 'max-w-full',
};

export default function Container({ children, className, size = 'lg' }: ContainerProps) {
  return <div className={cn('w-full mx-auto px-4 sm:px-6', sizes[size], className)}>{children}</div>;
}
